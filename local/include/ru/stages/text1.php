При обращении необходимо кратко указать сущность проблемы, кратко описать события, которые
              предшествовали обращению.
              Это делается по причине того, что адвокат может определить, насколько срочно необходимо приступить к
              оказанию помощи,
              часто случается, что она требуется немедленно.